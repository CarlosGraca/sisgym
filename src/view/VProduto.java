    private void jtProdutosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtProdutosMouseClicked
        if (evt.getClickCount() == 2) {
            try {
                limparProdutoCampos();
                jTabProd.setSelectedIndex(1);
                ativarDesativaBotoes(false);
                ativarDesativarClienteCampos(false);
                controllerProduto.manutencaoPesquisa();
                jbCancelar.setEnabled(true);

            } catch (IOException ex) {
                Logger.getLogger(VCliente.class
                        .getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jtProdutosMouseClicked

    private void jbApagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbApagarActionPerformed
        if (controllerProduto.manutencaoProduto("excluir", jtProd_cod.getText(), jtProd_nome.getText(), jtProd_valor.getText(), new java.sql.Date(jdProd_data.getDate().getTime()), jtProd_Quant.getText(), jtProd_CodBarra.getText(), Principal.id)) {
            limparProdutoCampos();
            ativarDesativaBotoes(false);
            ativarDesativaBotoes2(false);
            jbNovo.setEnabled(true);
        }
    }//GEN-LAST:event_jbApagarActionPerformed

