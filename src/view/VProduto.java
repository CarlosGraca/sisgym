    public void setPosicao() {
        Dimension d = this.getDesktopPane().getSize();
        this.setLocation((d.width - this.getSize().width) / 2, (d.height - this.getSize().height) / 2);
    }

    public void limparProdutoCampos() {
        jtProd_cod.setText("");
        jtProd_nome.setText("");
        jtProd_valor.setText("");
        jtProd_Quant.setText("");
        jtProd_CodBarra.setText("");
    }

    public void ativarDesativarClienteCampos(boolean habilitar) {
        jtProd_nome.setEditable(habilitar);
        jtProd_valor.setEditable(habilitar);
        jtProd_Quant.setEditable(habilitar);
        jtProd_CodBarra.setEditable(habilitar);
    }

    public void ativarDesativaBotoes(boolean habilitar) {
        jbGuardar.setEnabled(habilitar);
        jbCancelar.setEnabled(habilitar);
        jbNovo.setEnabled(habilitar);
        jbEditar.setEnabled(!habilitar);
        jbApagar.setEnabled(!habilitar);
        jbImprimir.setEnabled(!habilitar);
    }

    public void ativarDesativaBotoes2(boolean habilitar) {
        jbEditar.setEnabled(habilitar);
        jbApagar.setEnabled(habilitar);
    }

    private boolean validarCampos() {

        if (jtProd_nome.getText().equals("")) {
            jtProd_nome.setBorder(new LineBorder(Color.red));
            jtProd_nome.requestFocus();
            return false;
        }
        if (jtProd_valor.getText().equals("")) {
            jtProd_valor.setBorder(new LineBorder(Color.red));
            jtProd_valor.requestFocus();
            return false;
        }
        if (jtProd_Quant.getText().equals("")) {
            jtProd_Quant.setBorder(new LineBorder(Color.red));
            jtProd_Quant.requestFocus();
            return false;
        }
        return true;

    }

