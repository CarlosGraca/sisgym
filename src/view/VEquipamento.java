    private void jbGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbGuardarActionPerformed
        if (jtEqui_cod.getText().equals("")) {
            if (validarCampos()) {
                equipController.manutencaoEquipamento("inserir", jtEqui_cod.getText(), jtEqui_nome.getText(), jtEqui_marca.getText(), jtEqui_codEquip.getText());
                ativarDesativarClienteCampos(false);
                limparClienteCampos();
                ativarDesativaBotoes(true);
                destivarAtivarBotoes2(true);
                equipController.preencherJtableEquipamento();
            }
        } else {
            equipController.manutencaoEquipamento("alterar", jtEqui_cod.getText(), jtEqui_nome.getText(), jtEqui_marca.getText(), jtEqui_codEquip.getText());
            limparClienteCampos();
            ativarDesativaBotoes(true);
            destivarAtivarBotoes2(true);
            equipController.preencherJtableEquipamento();
        }
    }//GEN-LAST:event_jbGuardarActionPerformed

