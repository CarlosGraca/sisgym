    private void jtEquipamentosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtEquipamentosMouseClicked
        int linhaSelecionada = jtEquipamentos.getSelectedRow();
        jtEqui_cod.setText(jtEquipamentos.getValueAt(linhaSelecionada, 0).toString());
        jtEqui_nome.setText(jtEquipamentos.getValueAt(linhaSelecionada, 1).toString());
        jtEqui_marca.setText(jtEquipamentos.getValueAt(linhaSelecionada, 2).toString());
        jtEqui_codEquip.setText(jtEquipamentos.getValueAt(linhaSelecionada, 3).toString());
        jTabEqui.setSelectedComponent(jpEqui_dados);
        ativarDesativaBotoes(false);
        ativarDesativarClienteCampos(false);
    }//GEN-LAST:event_jtEquipamentosMouseClicked

