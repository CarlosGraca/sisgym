    private boolean validarCampos() {

        if (jtEqui_nome.getText().equals("")) {
            jtEqui_nome.setBorder(new LineBorder(Color.red));
            jtEqui_nome.requestFocus();
            return false;
        }
        if (jtEqui_marca.getText().equals("")) {
            jtEqui_marca.setBorder(new LineBorder(Color.red));
            jtEqui_marca.requestFocus();
            return false;
        }
        return true;

    }

