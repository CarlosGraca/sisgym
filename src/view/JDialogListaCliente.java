    private void jbEmiteCartaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbEmiteCartaoActionPerformed
        Conexao con = new Conexao();
        if (controllerCliente.seletPessoaPorNome(jcClienteNome)) {

            String nomeCliente = "Imprimir cartao do cliente " + jcClienteNome.getSelectedItem().toString() + " ?";
            int opcaoEscolhida = JOptionPane.showConfirmDialog(null, nomeCliente, "Confirmacão Inpressao ", JOptionPane.YES_OPTION);

            if (opcaoEscolhida == JOptionPane.YES_OPTION) {
                dispose();
                janela = 0;
                int idPessoa = controllerCliente.getIdPessoas();
                InputStream caminho = getClass().getResourceAsStream("/relatorios/RCartao.jrxml");
                con.EjectuarReporteFiltro(idPessoa,"pIdCliente", caminho);

            }
        }


    }//GEN-LAST:event_jbEmiteCartaoActionPerformed

