public String carregaImgagem() {
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Carregar imagem");

        fileChooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        FileNameExtensionFilter filter = new FileNameExtensionFilter(
                "PNG, JPG, GIF, and BMP Images", "PNG", "jpg", "gif", "bmp");
        fileChooser.setFileFilter(filter);
        
        // int a = fileChooser.showOpenDialog(null);
        if (fileChooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            File arquivo = fileChooser.getSelectedFile();//arquivo   
            
            caminho = arquivo.getAbsolutePath();
          //  JOptionPane.showMessageDialog(this, caminho);
        }
        
        return caminho;
    }
