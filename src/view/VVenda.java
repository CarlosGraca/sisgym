    private boolean validarCampos() {

        if (jtVen_nomeProduto.getText().equals("")) {
            jtVen_nomeProduto.setBorder(new LineBorder(Color.red));
            jtVen_nomeProduto.requestFocus();
            return false;
        }
        if (jtVen_valor.getText().equals("")) {
            jtVen_valor.setBorder(new LineBorder(Color.red));
            jtVen_valor.requestFocus();
            return false;
        }
        if (jtVen_Quant.getText().equals("")) {
            jtVen_Quant.setBorder(new LineBorder(Color.red));
            jtVen_Quant.requestFocus();
            return false;
        }
        return true;

    }
