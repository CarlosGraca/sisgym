    public void emitirReciboVenda() {
        Conexao con = new Conexao();
        String nomeCliente = "Imprimir este recibo de pagamento venda ";
        int opcaoEscolhida = JOptionPane.showConfirmDialog(null, nomeCliente, "Confirmacão Inpressao ", JOptionPane.YES_OPTION);
        if (opcaoEscolhida == JOptionPane.YES_OPTION) {
            InputStream caminho = getClass().getResourceAsStream("/relatorios/RReciboVenda.jrxml");
            con.EjectuarReporteFiltroString(String.valueOf(idVenda), "idPag", caminho);
        }
    }
    private void jtVen_QuantKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtVen_QuantKeyTyped
        char c = evt.getKeyChar();
        if (!jtVen_Quant.getText().equals("")) {
            if (c < '0' || c > '9') {
                evt.consume();
                if (flag == 3) {
                    totalItem = Double.parseDouble(jtVen_valor.getText()) * Integer.parseInt(jtVen_Quant.getText());
                    jTValorTotal.setText(String.valueOf(totalItem));
                }
            }
        }
    }//GEN-LAST:event_jtVen_QuantKeyTyped

