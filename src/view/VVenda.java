    private void jbNovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbNovoActionPerformed
        if (idVenda == 0) {
            controllerVenda.manutencaoVendaIncial(Principal.id, new java.sql.Date(jdVen_data.getDate().getTime()));
        }
        jTabCli.setSelectedComponent(jpVen_dados);
        ativarDesativaBotoes(false);
        ativarDesativarClienteCampos(true);
        limparClienteCampos();
        jbGuardar.setEnabled(false);
    }//GEN-LAST:event_jbNovoActionPerformed

    private void jbGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbGuardarActionPerformed
        if (validarCampos()) {
            if (jtVen_nomeCliente.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Error: Escolhe o cliente");
            } else {
                if (controllerVenda.manutencaoVendaFinal("inserirVenda", new java.sql.Date(jdVen_data.getDate().getTime()), Integer.parseInt(codigoCliente), Float.parseFloat(jTextFieldAPagar.getText()), Principal.id, idVenda)) {
                    limparClienteCampos();
                    emitirReciboVenda();
                    idVenda = 0;
                }
            }
        }


    }//GEN-LAST:event_jbGuardarActionPerformed

    private void jbCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbCancelarActionPerformed
        limparClienteCampos();
        ativarDesativaBotoes(true);
        ativarDesativarClienteCampos(false);
        if (idVenda != 0) {
            try {
                controllerVenda.cancelaVenda();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Error: ao cancelar venda " + ex);
            }
        }
    }//GEN-LAST:event_jbCancelarActionPerformed

